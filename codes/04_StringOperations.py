# string casees
print(name.lower())
print(name.upper())
print(name.title())
print(name.capitalize())
print(name.swapcase())
print(name.casefold())  # casefold() is used to remove case sensitivity of a string.
print(name.islower()) # it returns true if all characters in the string are in lowercase.
print(name.isupper()) # it returns true if all characters in the string are in uppercase.
print(name.istitle()) # it returns true if all characters in the string are in title case
print(name.isalpha()) # it returns true if all characters in the string are alphabets.
print(name.isalnum()) # it returns true if all characters in the string are alphanumeric.
print(name.isdecimal()) # it returns true if all characters in the string are decimal numbers.
print(name.isdigit()) # it returns true if all characters in the string are digits.
print(name.isidentifier()) # it returns true if the string is a valid identifier.
print(name.isnumeric()) # it returns true if all characters in the string are numeric.
print(name.isprintable()) # it returns true if all characters in the string are printable.
print(name.isspace()) # it returns true if all characters in the string are whitespace characters.
print(name.startswith('Z')) # it returns true if the string starts with the specified value.
print(name.endswith('n')) # it returns true if the string ends with the specified value.
print(name.find('Z')) # it returns the index of the first occurrence of the specified value.
print(name.rfind('Z')) # it returns the index of the last occurrence of the specified valu
print(name.count('Z')) # it returns the number of occurrences of the specified value.
print(name.replace('Z', 's')) # it replaces the specified value with the specified value.
print(name.split(' ')) # it splits the string into a list where each word is a list
print(name.join(' ')) # it joins the string with the specified value.
print(name.strip('Z')) # it removes the specified value from the start and end of the string
print(name.lstrip('Z')) # it removes the specified value from the start of the string
print(name.rstrip('k')) # it removes the specified value from the end of the string
